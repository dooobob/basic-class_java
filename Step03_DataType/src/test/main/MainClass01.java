package test.main;
/*
 * [ Java Data Type ]
 * 
 * 1. String type
 */
public class MainClass01 {
	public static void main(String[] args) {
		//String Type 데이터를 만들어서 콘솔에 출력
		System.out.println("김경태");
		
		//name이라는 String Type 저장소에
		//String Type 데이터를 기억시킨 후
		String name="해골";
		//String Type 데이터가 들어있는 변수명을 전달해서
		//문자열을 콘솔창에 출력하기
		System.out.println(name);
		
		name = "해골바가지";
		System.out.println(name);
	} 
}
